// UniqueString takes a slice of string and returns a slice with only unique elements.
func UniqueString(in []string, allowEmpty bool) []string {
	seen := make(map[string]bool)
	var out []string
	for _, v := range in {
		if _, isset := seen[v]; isset {
			continue
		}

		if !allowEmpty && v == "" {
			continue
		}

		seen[v] = true
		out = append(out, v)
	}

	return out
}
